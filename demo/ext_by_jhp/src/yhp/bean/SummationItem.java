package yhp.bean;

public class SummationItem extends Item {
	int summation;
	int count;

	public int getSummation() {
		return summation;
	}

	public void setSummation(int summation) {
		this.summation = summation;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
